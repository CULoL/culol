<h1>CLEMSON UNIVERSITY LEAGUE OF LEGENDS CLUB CONSTITUTION</h1>

<h2 class="featurette-heading">ARTICLE I<span class="text-muted"> NAME OF CLUB</span></h2>
<p><span class="text-muted">Section 1.</span>	The name of this club shall be Clemson University League of Legends Club.<p>

<h2 class="featurette-heading">ARTICLE II<span class="text-muted"> PURPOSE OF CLUB</span></h2>
<p><span class="text-muted">Section 1.</span>	The purpose of the club is to provide a fun community on League of Legends for any who want one, and to field a competitive, official Clemson University team for any event necessary.</p>

<h2 class="featurette-heading">ARTICLE III<span class="text-muted"> MEMBERSHIP</span></h2>
<p><span class="text-muted">Section 1.</span>	Club members may not be on academic or social probation. Members shall not be discriminated against for any reason including but not limited to age, color, disability, gender, national origin, race, religion, sexual orientation, veteran’s status, or generic information.</p>

<h2 class="featurette-heading">ARTICLE IV<span class="text-muted"> QUALIFICATION AND ELECTION OF OFFICERS</span></h2>
<p><span class="text-muted">Section 1.</span>	Club officers must be currently enrolled, have a minimum of a 2.0 cumulative Grade Point Average (GPA), must maintain 12 hours, and must not be on academic or social probation.</p>
<p><span class="text-muted">Section 2.</span>	Officers will be a President, Vice-President, Secretary, Treasurer, Head Mentor, and Event Organizer.</p>
<p><span class="text-muted">Section 3.</span>	Club elections will be held no later than the final week in March. The election will be done by secret ballot by club members who have attended at least two meetings.</p>

<h2 class="featurette-heading">ARTICLE V<span class="text-muted"> DUTIES OF THE OFFICERS</span></h2>
<p><span class="text-muted">Section 1.</span>	President</p>
<ul>
<li>Preside over all meetings.</li>
<li>Call special meetings.</li>
<li>Carry out the provisions of the constitution.</li>
<li>Appoint committees and chairpersons.</li>
<li>Oversee all committee activities.</li>
</ul>
<p><span class="text-muted">Section 2.</span>	Vice-President</p>
<ul>
<li>Assume the duties of the president in their absence.</li>
<li>Perform any duties delegated by the president.</li>
<li>Keep secondary log of financial records.</li>
</ul>
<p><span class="text-muted">Section 3.</span>	Secretary</p>
<ul>
<li>Record and keep accurate minutes of all meetings.</li>
<li>Act as correspondence clerk.</li>
<li>Print and distribute agenda for all meetings.</li>
</ul>
<p><span class="text-muted">Section 4.</span>	Treasurer</p>
<ul>
<li>Handle funds and finances for club.</li>
<li>Keep financial records.</li>
<li>Pay bills and release funds as voted by membership.</li>
<li>Make financial reports at meetings.</li>
</ul>
<p><span class="text-muted">Section 5.</span>	Head Mentor</p>
<ul>
<li>Help any and all members willing to improve.</li>
<li>Form and preside over Mentor Group.</li>
<li>Preside over coaches.</li>
</ul>
<p><span class="text-muted">Section 6.</span>	Event Organizer</p>
<ul>
<li>Act as public point of contact for the club.</li>
<li>Respond to any external queries about the club.</li>
<li>Organize and publicize all club events.</li>
</ul>

<h2 class="featurette-heading">ARTICLE VI<span class="text-muted"> EXECUTIVE COUNCIL</span></h2>

<p><span class="text-muted">Section 1.</span>	The Executive Council will consist of the President, Vice President, Secretary, Treasurer, Head Mentor, and Event Organizer who shall meet as often as necessary</p>


<p><span class="text-muted">Section 2.</span>	Duties of the Executive Council</p>
<ul>
<li>To formulate policy of the club between regular meetings and in case of emergencies, subject to approval of the general membership.</li>
<li>To execute policies determined by the general membership.</li>
<li>To govern activities of the club.</li>
<li>To compile agenda for general meetings.</li>
</ul>

<h2 class="featurette-heading">ARTICLE VII<span class="text-muted"> IMPEACHMENT/REMOVAL AND REPLACEMENT OF OFFICERS AND MEMBERS</span></h2>


<p><span class="text-muted">Section 1.</span>	All elected officers and club members may be subjected to impeachment and removal by a two-thirds majority vote of the total membership.</p>


<p><span class="text-muted">Section 2.</span>	Grounds for impeachment are negligence and any form of misconduct which is damaging to the club. An officer/member may be impeached/removed only under the following conditions: there must be a quorum present during impeachment/removal; two-thirds of the membership must vote; prior to impeachment/removal; there must be one week’s notice of intent publicized.</p>


<p><span class="text-muted">Section 3.</span>	Any officer vacancies shall be filled by an election held within one week.</p>

<h2 class="featurette-heading">ARTICLE VIII<span class="text-muted"> MEETINGS</span></h2>

<p><span class="text-muted">Section 1.</span>	There must be public notice of club meetings at least five (5) school days in advance.</p>


<p><span class="text-muted">Section 2.</span>	There must be a meeting at least once a month held at Clemson University. During the summer break, no meetings are mandatory.</p>

<p><span class="text-muted">Section 3.</span>	There must be a quorum (50% plus 1 of the active membership) present in order to take care of financial action.</p>


<p><span class="text-muted">Section 4.</span>	Minutes will be kept of all financial action with the club Vice President.</p>


<p><span class="text-muted">Section 5.</span>	The treasurer will need to make a written and oral report at least once a month at the meeting.</p>


<h2 class="featurette-heading">ARTICLE IX<span class="text-muted"> FACULTY ADVISER</span></h2>

<p><span class="text-muted">Section 1.</span>	The role of the faculty adviser is to:</p>
<ul>
<li>Serve as the official staff representative of the college.</li>
<li>Work closely with the club to ensure a cooperative relationship between the adviser and the club membership.</li>
<li>Assist each officer of the club in understanding their duties.</li>
<li>Give particular attention to the financial activities of the group in order to prevent the incurring of organizational debts for succeeding members to pay.</li>
<li>Assist students to understand apply democratic principles within their own organizations and in working with others.</li>
<li>Be present at all major official club meetings and activities, and to advise students of the policies and procedures which they must follow as a club/organization.</li>
<li>Ensure that all reasonable steps are taken to ensure the safety and welfare of club members.</li>
<li>Ensure that appropriate college policies are upheld.</li>
<li>To sign all club requisitions for the club, and to make sure that 1) their student treasurer or president or vice president signs it and 2) that the expenditure is correct within all existing policies.</li>
</ul>

<h2 class="featurette-heading">ARTICLE X<span class="text-muted"> RESPONSIBILITIES</span></h2>


<p><span class="text-muted">Section 1.</span>	This club accepts full financial responsibility for all activities that bear its name as official sponsor, and will adhere to college regulations. All publicity for an event must bear the name of the sponsoring club.</p>

<h2 class="featurette-heading">ARTICLE XI<span class="text-muted"> RIGHT TO ACT</span></h2>


<p><span class="text-muted">Section 1.</span>	Any club officer or club member does not have the right to incur any debt or become involved in any business under the title or by implying the title of a club in any way unless given full authority to do so by the club.</p>

<h2 class="featurette-heading">ARTICLE XII<span class="text-muted"> AMMENDMENTS</span></h2>


<p><span class="text-muted">Section 1.</span>	Any amendment change requires a two-thirds vote at a general club meeting.</p>

