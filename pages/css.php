<div class="row marketing">
    <div class="col-lg-6">
		<h2>Structure</h2>
        <h4>Season Stage</h4>
		<p>A stage with games spread out over 12 weeks, where all 9 teams play each other twice.
		<h4>Final Tournament</h4>
		<p>This is a standard bracket tournament, best of three with the finals being best of five, exactly the way we saw the Season 3 finals. This tournament will be held over a three week period, mostly on weekends, which is subject to change due to the approaching finals week.</p>
    </div>
	
    <div class="col-lg-6">
		<h2>General information</h2>
		<p>The schedule can be found on our <a href="page.php?page=calender.php">Calender</a> page.</p>
        <h4>Season Timeline (Tentative)</h4>
		<p>Season Stage: Monday, January 13th - Sunday, March 31th (12 weeks)
			<br/>Final Tournament: Monday, April 7th - Sunday, April 27th (3 weeks)</p>
		<h4>Prizing</h4>
		<p>Riot will be sponsoring the final tournament, however Clemson League of Legends Club is also offering a small prize for all teams that complete all of their matches.  There will be additional prizes for teams who make it for group stage and the final tournament, possibly including jerseys and mousepads.  The winning team also receives their name on the official CCS plaque.
