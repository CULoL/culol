			<br/>Final Tournament: Monday, April 7th - Sunday, April 27th (3 weeks)</p>
		<h4>Prizing</h4>
		<p>Riot will be sponsoring the final tournament, however Clemson League of Legends Club is also offering a small prize for all teams that complete all of their matches.  There will be additional prizes for teams who make it for group stage and the final tournament, possibly including jerseys and mousepads.  The winning team also receives their name on the official CCS plaque.
