<h2>FAQ</h2>
<h4>What if I hit diamond in between registration and the end of the season?</h4>
<p>You will not be penalized, and may remain on your team. This rule is primarily to prevent a team from coming in with challenger tier players and sweeping the competition to get an "easy" prize.</p>

<h4>What if my team gets signed up for a match on a day I can't make it?</h4>
<p>We will a) have a time where we ask all teams what days would be best for them on a general basis, b) have work-arounds and reschedule games if need be (there is an empty week within the 8 weeks of the Season Stage to account for this), and finally, c) we will allow for a team to use a sub if need be.</p>

<h4>What if I have a major assignment coming up and can't afford to skip it?</h4>
<p>This depends entirely on the nature of said assignment. If you don't think you can manage your time well enough to give yourself an hour or two a week during the Season Stage, you probably shouldn't sign up. There will be some issues with the Group Stage and the Final Tournament, primarily due to timing, but we have multiple full weeks allotted to each of those stages to allow us to plan around everyone.</p>