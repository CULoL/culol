		<h2>Established in April 2013</h2>
		<p><a href="http://http://na.leagueoflegends.com/">League of Legends</a> is a massive online battle arena (MOBA) game with over 12 million players 

		worldwide. The Clemson University League of Legends Club is committed to providing both a fun and engaging community for players at Clemson University 

		where players can share their love of the game, as well as to provide a competitive team for Clemson University with the purpose of competing against other universities

		and amateur teams in collegiate leagues and tournaments.</p>

		<p>The club regularly meets every other Wednesday, usually on campus at 8:00PM in Kinard G01 or 301.  Meetings are held

		to discuss upcoming and past events as well as to provide a time for players to play with each other.</p>

		<p>CULoL holds many special events such as tournament viewing parties, LAN parties, and Riot-sponsored tournaments, for more information view our calender!</p>

